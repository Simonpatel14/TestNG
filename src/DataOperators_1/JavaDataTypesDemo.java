package DataOperators_1;

public class JavaDataTypesDemo {

	public static void main(String[] args) {
		
	//data types: ( this is called comment section)
		//data type is type of the data which is statically type language
		
		//statically type language:
		//1.primitive Data Types:   
		       //Boolean type: boolean : true/false
               //Numeric Type: Integral type:	
		                      //Integer: byte,short,int ,long 
                        //   : Floating-type: float,double
		       //Character Type: char
		
		
		       //byte:
	       	  //Size: 1 byte = 8 bits
		     //Range: -128 to 127
		      byte b = 10;
		      b = 20;
		      System.out.println(b+30);
		      byte b1 = -5;
		      byte b2 = 0;
		      
		      
		      //short:
		      //Size: 2 bytes = 16 bits
		      //range: -32768 to 32767
		      short sh = 100;
		      System.out.println(sh);
		      
		      //int: we can store only integer value not decimal
		      //Size: 4 bytes = 32 bits
		      //range : -2147483648 to 2147483647
		      int i = 10000;
		      int j =i+20;
		      System.out.println(i+j);
		      
		      //long:
		      //size: 8 bytes = 64 bits
		      long l = 12310000;
		      long phone = 8889554;
		      //cc,phone number,distance between earth & moon
		      
		      //float :we can store int value but in console window it shows us .00
		      //size : 4 bytes = 32 bits
		      //Range : after  . can take up to 7 digits
		      float f = 10.35f;
		      float f1 = (float)32.55;
		      System.out.println(f+f1);
		      float f2 = 100;
		      System.out.println(f2);
		      
		      //double :
		      //Size : 8 bytes =64 bits
		      //range : after . can take up to 16 digits
		      double d = 12.33333;
		      
		
		      //char : 1 digit value
		      //Size : 2 bytes =16 bits
		      char c = 'a';
		      char c1 = '5';
		      int v= 10;
		      System.out.println(c);
		      System.out.println("------------");
		      System.out.println(c+v);
		      
		      
		      
		      //boolean : true/false
		      //size : ~1 bit = 1 bit
		      boolean flag = true;
		      boolean isActive = false;
		      boolean isDisplayed = true;
		      System.out.println(flag);
		      
		      int total = 100;
		      total = 300;
		      System.out.println(total);
		      
		      
		      
		      
		      
		   
		      
		      
		      
		       
	}

}
